def setup_ik(dof_idx: dict) -> tuple:
    """Initialize IK backends: cuRobo (GPU) + numpy fallback always loaded.

    When cuRobo is available it is used as the primary solver. Numpy IK is
    always loaded alongside cuRobo so that envs whose cuRobo solve fails can
    fall back to incremental Jacobian steps (numpy always makes progress even
    when not fully converged, breaking the stuck-at-seed loop).

    Must be called AFTER SimulationApp starts.
    Returns (ik_backend: str, ik_open, ik_closed).
    Pass the result to init_runtime()."""
    import sys as _sys, os as _os
    import boilerplate_runtime as _brt
    # Store dof_idx early so run_ik_diagnostics (called before init_runtime) can use it
    _brt._dof_idx = dof_idx
    _os.environ["TORCH_CUDA_ARCH_LIST"] = "8.9;12.0"
    # Insert our src/ dir first so our updated solve_ik_curobo.py takes priority
    # over the copy in _IK_SCRIPT_DIR (which uses the old cuRobo v1 API).
    _SRC_DIR = _os.path.dirname(_os.path.abspath(__file__))
    if _SRC_DIR not in _sys.path:
        _sys.path.insert(0, _SRC_DIR)
    if _IK_SCRIPT_DIR not in _sys.path:
        _sys.path.append(_IK_SCRIPT_DIR)

    # Always try to load numpy IK so it can serve as fallback for cuRobo failures.
    try:
        from solve_ik_numpy import NumpyArmIK as _NumpyArmIK
        _brt._IK_NUMPY = _NumpyArmIK(
            urdf_path=_IK_URDF_PATH,
            ee_link_name="xhand_mount_right",
            arm_joint_names=[f"Actuator{i}" for i in range(1, 8)],
            damping=1e-6, max_iters=100, eps=1e-3, dq_max=0.15,
        )
        print("IK_NUMPY_FALLBACK: loaded (will step failed cuRobo envs forward)", flush=True)
    except Exception as _numpy_load_err:
        print(f"IK_NUMPY_FALLBACK: unavailable ({_numpy_load_err})", flush=True)
        _brt._IK_NUMPY = None

    try:
        from solve_ik_curobo import (CuRoboArmIK as _CuRoboArmIK, HAND_CLOSED as _HAND_CLOSED_DICT,
                                      _URDF_WORLD_TO_BASE_XYZ as _WB_XYZ,
                                      _URDF_WORLD_TO_BASE_RPY_Y as _WB_RPY_Y)
        import numpy as _np_ik
        ik_open = _CuRoboArmIK(
            urdf_path=_IK_URDF_PATH,
            ee_link_name="xhand_mount_right",
            arm_joint_names=[f"Actuator{i}" for i in range(1, 8)],
            num_seeds=200,
            position_threshold=0.005,
            world_to_base_xyz=_np_ik.array(_WB_XYZ),
            world_to_base_rpy_y=float(_WB_RPY_Y),
        )
        ik_closed = ik_open.with_hand_pose(_HAND_CLOSED_DICT)
        ik_backend = "curobo"
        print(
            f"IK_READY: curobo n_arm_dof={ik_open.n_arm_dofs} "
            f"joints={ik_open.arm_joint_names}",
            flush=True,
        )
    except Exception as _curobo_err:
        print(f"IK_CUROBO_UNAVAILABLE: {_curobo_err}", flush=True)
        if _brt._IK_NUMPY is not None:
            ik_open    = _brt._IK_NUMPY
            ik_closed  = _brt._IK_NUMPY
            ik_backend = "numpy"
            print(
                f"IK_READY: numpy_ik n_arm_dof={ik_open.n_arm_dofs} "
                f"joints={ik_open.arm_joint_names}",
                flush=True,
            )
        else:
            print("IK_UNAVAILABLE: no solver available — IK calls will return seed unchanged", flush=True)
            ik_open = ik_closed = None
            ik_backend = "none"

    print(f"IK_BACKEND: {ik_backend}", flush=True)
    return ik_backend, ik_open, ik_closed


