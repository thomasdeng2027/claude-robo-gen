# ─── JSON helpers ──────────────────────────────────────────────────────────────

class _NumpyEncoder(json.JSONEncoder):
    def default(self, o):
        if isinstance(o, np.integer):  return int(o)
        if isinstance(o, np.floating): return float(o)
        if isinstance(o, np.ndarray):  return o.tolist()
        return super().default(o)


def json_safe(obj):
    """Recursively strip numpy scalars/arrays so the result is json.dumps-safe.
    Use for any json.dumps call that may touch numpy values, e.g.:
        json.dumps(json_safe({"pos": arr[0], "val": arr[i,j]}))
    Individual numpy scalar indexing (arr[i,j]) is NOT auto-converted — always
    wrap with float(arr[i,j]) or use this helper on the whole dict."""
    if isinstance(obj, dict):
        return {k: json_safe(v) for k, v in obj.items()}
    if isinstance(obj, (list, tuple)):
        return [json_safe(v) for v in obj]
    if isinstance(obj, np.integer):  return int(obj)
    if isinstance(obj, np.floating): return float(obj)
    if isinstance(obj, np.ndarray):  return obj.tolist()
    return obj


# ─── Quaternion helpers (wxyz convention) ──────────────────────────────────────

def _qnorm(q):
    q = np.asarray(q, dtype=np.float64)
    return q / (np.linalg.norm(q) + 1e-12)


def _qconj(q):
    q = np.asarray(q, dtype=np.float64)
    return np.array([q[0], -q[1], -q[2], -q[3]])


def _qmul(q1, q2):
    w1, x1, y1, z1 = q1; w2, x2, y2, z2 = q2
    return np.array([
        w1*w2 - x1*x2 - y1*y2 - z1*z2,
        w1*x2 + x1*w2 + y1*z2 - z1*y2,
        w1*y2 - x1*z2 + y1*w2 + z1*x2,
        w1*z2 + x1*y2 - y1*x2 + z1*w2,
    ], dtype=np.float64)


def _qmat(q):
    w, x, y, z = _qnorm(q)
    return np.array([
        [1-2*(y*y+z*z), 2*(x*y-w*z),   2*(x*z+w*y)  ],
        [2*(x*y+w*z),   1-2*(x*x+z*z), 2*(y*z-w*x)  ],
        [2*(x*z-w*y),   2*(y*z+w*x),   1-2*(x*x+y*y)],
    ], dtype=np.float64)


def _qrotate(q, v):
    q = _qnorm(q)
    qv = np.array([0.0, v[0], v[1], v[2]])
    r = _qmul(_qmul(q, qv), _qconj(q))
    return r[1:]


