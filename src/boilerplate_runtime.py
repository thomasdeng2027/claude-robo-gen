# ── World-to-base_link frame transform (USD pipeline) ─────────────────────────
# The robot USD was converted from realkinova_xhand_right.urdf which has a
# Frame transform: env-local (Isaac Sim) → cuRobo base_link frame.
# run_ik_diagnostics() reads the actual base_link pose from the USD stage and
# updates these values at runtime. Until then, defaults are identity (no-op)
# so IK targets are passed through unchanged — safe when base_link is near origin.
_WORLD_TO_BASE_T: np.ndarray = np.zeros(3, dtype=np.float64)
_WORLD_TO_BASE_ACTIVE: bool  = False  # auto-detected by run_ik_diagnostics
_R_WB: np.ndarray = np.eye(3, dtype=np.float64)


def disable_world_to_base_transform():
    """Call this when the robot is loaded from URDF (base_link at env origin).
    Disables the Isaac-Sim-world → cuRobo-base_link frame correction so that
    IK targets are passed directly to cuRobo without any frame conversion."""
    import boilerplate_runtime as _brt
    _brt._WORLD_TO_BASE_ACTIVE = False
    print("IK_FRAME: world_to_base transform DISABLED (URDF-loaded robot, frames aligned)", flush=True)


def run_ik_diagnostics(stage, robot_prim_path: str, init_joints_full_1d=None, ik_solver=None):
    """Read actual base_link world pose from stage, auto-calibrate IK frame transform,
    and run FK checks.

    Auto-calibration logic:
    - If base_link prim IS found in the stage (URDF-loaded robot): use its actual world
      position as the translation. The URDF-hardcoded transform is correct.
    - If base_link prim is NOT found (USD-loaded robot with merged fixed joints):
      the robot USD root IS base_link, so identity transform is correct. The hardcoded
      URDF offset [0.2032, -0.127, 0.1524] + 45°Y is WRONG — disable it.

    Must be called AFTER setup_ik() and after world.reset()/robots.initialize().
    Do NOT call ik_solver.solve_pose here — that corrupts the batch-size state.
    """
    import boilerplate_runtime as _brt
    import numpy as _np

    solver = ik_solver if ik_solver is not None else _brt._IK_OPEN
    if solver is None or not hasattr(solver, "arm_joint_names"):
        print("IK_DIAG: no IK solver available, skipping diagnostics", flush=True)
        return

    # ── 1. Read base_link world pose from stage and auto-calibrate transform ───
    try:
        from pxr import UsdGeom as _UsdGeom
        _base_path = robot_prim_path.rstrip("/") + "/base_link"
        _base_p = stage.GetPrimAtPath(_base_path)
        _base_prim_found = _base_p.IsValid()

        if _base_prim_found:
            # URDF-loaded robot: base_link prim exists at correct offset from root
            _xf = _UsdGeom.Xformable(_base_p).ComputeLocalToWorldTransform(0)
            _t  = _xf.ExtractTranslation()
            _T_info = _np.array([float(_t[0]), float(_t[1]), float(_t[2])], dtype=_np.float64)
            print(f"IK_DIAG: base_link found at world pos: {[round(v,4) for v in _T_info.tolist()]} — URDF transform is valid", flush=True)
        else:
            # USD-loaded robot: base_link merged into root — root IS base_link at origin.
            # The URDF offset [0.2032, -0.127, 0.1524] + 45°Y is WRONG here.
            # Disable it so IK targets pass through unchanged (world frame = base frame).
            _p = stage.GetPrimAtPath(robot_prim_path)
            _xf = _UsdGeom.Xformable(_p).ComputeLocalToWorldTransform(0)
            _t  = _xf.ExtractTranslation()
            _T_info = _np.array([float(_t[0]), float(_t[1]), float(_t[2])], dtype=_np.float64)
            print(f"IK_DIAG: base_link not found (USD with merged joints), robot root at: {[round(v,4) for v in _T_info.tolist()]}", flush=True)

            if solver._transform_active and _np.linalg.norm(solver._t_wb) > 0.01:
                # Disable the wrong URDF transform; world frame = base_link frame
                for _s in [solver] + ([_brt._IK_CLOSED] if _brt._IK_CLOSED not in (None, solver) else []):
                    if _s is not None and hasattr(_s, '_t_wb'):
                        _s._t_wb           = _np.zeros(3, dtype=_np.float64)
                        _s._R_wb           = _np.eye(3,  dtype=_np.float64)
                        _s._R_wb_T         = _np.eye(3,  dtype=_np.float64)
                        _s._transform_active = False
                print("IK_CALIB: disabled URDF transform — USD robot root = base_link, using identity", flush=True)
            else:
                print("IK_DIAG: transform already identity or inactive", flush=True)
    except Exception as _e:
        print(f"IK_DIAG: stage pose read skipped: {_e}", flush=True)

    # ── 2. FK check: print cuRobo FK world-frame position at init pose ────────
    # Uses compute_kinematics (NOT ik_solver.solve_pose) — safe, no state side-effects.
    try:
        if init_joints_full_1d is not None:
            import torch as _torch
            _arm_names = solver.arm_joint_names
            _arm_q = _np.array([float(init_joints_full_1d[_brt._dof_idx[jn]])
                                 for jn in _arm_names if jn in _brt._dof_idx],
                                dtype=_np.float32)
            if len(_arm_q) == len(_arm_names):
                _js = solver._make_joint_state(_arm_q)
                with _torch.no_grad():
                    _kin = solver._planner.compute_kinematics(_js)
                _ee_base = _kin.tool_poses.position[0, 0, 0].cpu().numpy().astype(_np.float64)
                _ee_world = solver.base_to_world(_ee_base)
                print(f"IK_DIAG: cuRobo FK base-frame:  {[round(v,4) for v in _ee_base.tolist()]}", flush=True)
                print(f"IK_DIAG: cuRobo FK world-frame: {[round(v,4) for v in _ee_world.tolist()]} (post-calibration)", flush=True)
    except Exception as _fk_e:
        print(f"IK_DIAG: FK check failed: {_fk_e}", flush=True)


