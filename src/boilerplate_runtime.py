def _save_frame():
    _frame_counter[0] += 1
    try:
        import cv2
        _rgb = _rgb_annotator.get_data()
        if _rgb is not None and hasattr(_rgb, "size") and _rgb.size > 0:
            cv2.imwrite(
                f"{_frames_dir}/frame_{_frame_counter[0]:06d}.png",
                cv2.cvtColor(_rgb, cv2.COLOR_RGB2BGR),
            )
    except Exception:
        pass


def world_step_auto_render(render=False):
    _step_counter[0] += 1
    if _step_counter[0] % _RENDER_EVERY == 0:
        _orig_world_step(render=True)
        try:
            import omni.replicator.core as _rep
            _rep.orchestrator.step(rt_subframes=1)
        except Exception:
            pass
        _save_frame()
    else:
        _orig_world_step(render=False)


def solve_ik_env0(target_pos, target_quat_wxyz, seed_joints_1d):
    """Single-env IK in env-0 frame. Returns full (n_dof,) joint vector.
    Uses whichever backend is available. Returns seed on failure."""
    if _IK_OPEN is None:
        return np.array(seed_joints_1d, dtype=np.float64)
    seed_1d = np.asarray(seed_joints_1d, dtype=np.float64).flatten()
    try:
        t_pos  = np.asarray(target_pos, dtype=np.float64).flatten()[:3]
        q_init = {jn: float(seed_1d[_dof_idx[jn]])
                  for jn in _IK_OPEN.arm_joint_names if jn in _dof_idx}
        q_sol, _ok, _err = _IK_OPEN.solve_ik_position(
            target_pos=t_pos, q_init=q_init, reg_weight=0.01)
        if not _ok:
            print(f"IK_WARN: err={_err:.4f}", flush=True)
        result_dof = seed_1d.copy()
        for jn, val in q_sol.items():
            if jn in _dof_idx:
                result_dof[_dof_idx[jn]] = float(val)
        return result_dof
    except Exception as _e:
        import traceback as _tb
        print(f"IK_EXCEPTION: {_e}\n{_tb.format_exc()}", flush=True)
        return seed_1d.copy()


def get_ik_ee_quats_all_envs(cmds_full: np.ndarray) -> np.ndarray:
    """Return (N, 4) xhand_mount_right wxyz quaternions via FK from current arm joints.

    Uses the CuRobo FK model to compute the exact orientation of the IK EE link
    (xhand_mount_right) for each env.  Call this after Phase 2a to capture the
    wrist orientation found by the cold-start IK, then pass the result as
    ``target_quats`` to ``solve_ik_batch_all_envs`` to constrain wrist orientation.
    prevent the wrist from rotating while the arm moves closer to the object.

    Falls back to identity quaternions if cuRobo FK is unavailable.
    """
    solver = _IK_OPEN
    if solver is None or not hasattr(solver, "fk_pose_batch"):
        N = len(cmds_full)
        return np.tile([1., 0., 0., 0.], (N, 1)).astype(np.float64)
    arm_col   = [_dof_idx[jn] for jn in solver.arm_joint_names if jn in _dof_idx]
    seeds_arm = np.asarray(cmds_full, dtype=np.float64)[:, arm_col]   # (N, 7)
    _, quats  = solver.fk_pose_batch(seeds_arm)  # (N, 4) wxyz
    return quats


def get_ik_ee_poses_all_envs(cmds_full: np.ndarray):
    """Return FK positions (N,3) and wxyz quaternions (N,4) of xhand_mount_right.

    IMPORTANT — pass the RIGHT joint array depending on your question:

      get_ik_ee_poses_all_envs(cmds)                        ← FK on IK-solution joints.
        Answers: "where does the IK WANT xhand_mount_right to be?"
        Error vs IK target is always ~0 (it just checks the IK algebra). This does
        NOT tell you anything about the physical robot in Isaac Sim.

      get_ik_ee_poses_all_envs(robots.get_joint_positions()) ← FK on ACTUAL robot joints.
        Answers: "where is xhand_mount_right ACTUALLY in physics right now?"
        Compare this to cmds FK to check convergence; compare to knuckle centroid
        (get_ee_pos_all_envs) to calibrate the xhand_mount_right → knuckle offset.

    Note: xhand_mount_right is approximately IK_EE_MOUNT_ADJ_M (~0.10 m) BEHIND the
    knuckle centroid along the approach direction. This offset must be added to IK
    targets so the knuckle centroid lands at the desired standoff from the object:
        ik_target = obj_pos + approach_dir * (standoff_m + IK_EE_MOUNT_ADJ_M)

    Args:
        cmds_full: (N, n_dof) full joint array — pass IK solution OR actual joints.

    Returns:
        positions : (N, 3) float64 — xhand_mount_right world-frame position per env
                    (env_offsets already added).
        quats     : (N, 4) float64 — wxyz quaternion per env.
    Falls back to zeros/identity if cuRobo FK unavailable.
    """
    solver = _IK_OPEN
    N = len(cmds_full)
    if solver is None or not hasattr(solver, "fk_pose_batch"):
        return np.zeros((N, 3), dtype=np.float64), np.tile([1., 0., 0., 0.], (N, 1)).astype(np.float64)
    arm_col   = [_dof_idx[jn] for jn in solver.arm_joint_names if jn in _dof_idx]
    seeds_arm = np.asarray(cmds_full, dtype=np.float64)[:, arm_col]   # (N, 7)
    positions, quats = solver.fk_pose_batch(seeds_arm)  # (N,3), (N,4)
    # FK positions are in env-0 frame; add env offsets to get world frame
    positions_world = positions + _env_offsets
    return positions_world, quats


def solve_ik_for_env(env_i, target_world, target_quat_wxyz, seed_joints_1d):
    """Per-env IK (subtracts env offset). Kept for compatibility; prefer solve_ik_batch_all_envs."""
    t0 = np.asarray(target_world, dtype=np.float64) - _env_offsets[env_i]
    return solve_ik_env0(t0, target_quat_wxyz, seed_joints_1d)


def solve_ik_batch_all_envs(targets_world, seeds_full, closed_hand=False,
                             target_quats=None, servo=False, servo_retract=None):
    """Solve IK for ALL N_ENVS in one call. USE THIS instead of the per-env loop.

    With cuRobo backend this is a single GPU dispatch — O(1) regardless of N_ENVS.
    With numpy fallback it runs the sequential loop internally.

    Args:
        targets_world : (N_ENVS, 3) desired EE world-frame position per env.
        seeds_full    : (N_ENVS, n_dof) current joint positions.
        closed_hand   : False for Phases 2 & 3 (approach/grasp, hand open).
                        True  for Phase 4 (transport, hand closed).
        target_quats  : (N_ENVS, 4) desired EE wrist orientations (wxyz), or None.
        servo         : Deprecated — ignored.  Always uses cold-start.
        servo_retract : Deprecated — ignored.
    Returns:
        cmds : (N_ENVS, n_dof) float64 — arm joints replaced by IK solution,
               hand joints copied unchanged from seeds_full.
               Rows where IK fails retain the seed arm joints."""
    solver = _IK_CLOSED if closed_hand else _IK_OPEN
    cmds   = np.asarray(seeds_full, dtype=np.float64).copy()
    if solver is None:
        return cmds
    tgts_env0 = np.asarray(targets_world, dtype=np.float64) - _env_offsets   # (N,3)

    # ── Frame transform: Isaac Sim env-local → cuRobo base_link frame ────────
    if _WORLD_TO_BASE_ACTIVE:
        tgts_env0 = (tgts_env0 - _WORLD_TO_BASE_T) @ _R_WB

    arm_col   = [_dof_idx[jn] for jn in solver.arm_joint_names if jn in _dof_idx]
    seeds_arm = cmds[:, arm_col]                                               # (N,7)
    # Prepare orientation targets: None (pos-only) or (N,4) wxyz array
    _tgt_quats = None
    if target_quats is not None:
        _tgt_quats_arr = np.asarray(target_quats, dtype=np.float64)
        if _tgt_quats_arr.ndim == 2 and _tgt_quats_arr.shape == (len(cmds), 4):
            _tgt_quats = _tgt_quats_arr.copy()
            for _ri in range(len(_tgt_quats)):
                if not np.any(np.isfinite(_tgt_quats[_ri])):
                    _tgt_quats[_ri] = [1.0, 0.0, 0.0, 0.0]
        elif _tgt_quats_arr.ndim == 1 and len(_tgt_quats_arr) == 4:
            _tgt_quats = np.tile(_tgt_quats_arr, (len(cmds), 1))
    if _IK_BACKEND == "curobo":
        q_batch, _succ = solver.solve_ik_batch(tgts_env0, seeds_arm,
                                                target_quats=_tgt_quats)
        n_fail = int((~_succ).sum())
        if n_fail > 0:
            _brt._ik_fail_step_count = getattr(_brt, '_ik_fail_step_count', 0) + 1
            if _brt._ik_fail_step_count <= 3 or _brt._ik_fail_step_count % 50 == 0:
                _dists = np.linalg.norm(tgts_env0, axis=1)
                print(f"IK_FAIL_COUNT: {n_fail}/{len(_succ)} "
                      f"z=[{tgts_env0[:,2].min():.3f},{tgts_env0[:,2].max():.3f}] "
                      f"dist_from_base=[{_dists.min():.3f},{_dists.max():.3f}]", flush=True)
        else:
            _brt._ik_fail_step_count = 0

        for out_i, col in enumerate(arm_col):
            cmds[:, col] = q_batch[:, out_i]
        return cmds
    # numpy fallback — sequential CPU loop (target_quats ignored for numpy backend)
    for ei in range(len(cmds)):
        q_init = {jn: float(seeds_arm[ei, k])
                  for k, jn in enumerate(solver.arm_joint_names)}
        q_sol, _ok, _err = solver.solve_ik_position(
            tgts_env0[ei], q_init=q_init, reg_weight=0.01)
        for jn, val in q_sol.items():
            if jn in _dof_idx:
                cmds[ei, _dof_idx[jn]] = float(val)
    return cmds


def get_object_contact_forces():
    """Return (N_ENVS, 3) net contact forces on each object in world frame (Newtons).
    Returns a zero array gracefully if contact tracking is unavailable."""
    try:
        raw = _objects.get_net_contact_forces(dt=_PHYSICS_DT)
        if raw is None:
            return np.zeros((_N_ENVS, 3), dtype=np.float64)
        return np.asarray(raw, dtype=np.float64)
    except Exception:
        return np.zeros((_N_ENVS, 3), dtype=np.float64)


def keypoint_max_dist_batch(positions, quats, goal_kps=None):
    """Returns (N,) array of max keypoint distance per env.
    goal_kps: (N,4,3) per-env goals  → compare each env to its own goal  [default: GOAL_KEYPOINTS_BATCH]
              (4,3)   shared goal     → broadcast to all envs              [legacy single-goal mode]
    NOTE: call with no goal_kps argument to get per-env evaluation using the aligned batch goals."""
    kps  = compute_keypoints_batch(positions, quats)   # (N,4,3)
    _gkp = _GOAL_KEYPOINTS_BATCH if goal_kps is None else np.asarray(goal_kps, dtype=np.float64)
    if _gkp.ndim == 2:   # (4,3) shared → broadcast
        dists = np.linalg.norm(kps - _gkp[None], axis=2)
    else:                # (N,4,3) per-env
        dists = np.linalg.norm(kps - _gkp, axis=2)
    return dists.max(axis=1)   # (N,)
